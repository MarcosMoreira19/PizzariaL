    private void gerarContaActionPerformed(java.awt.event.ActionEvent evt) {                                           
        //id do pedido
        int orderno = (int) (Math.random() * 100);
        jTextField1.setText("" + orderno);
        String custname
                = jTextField2.getText();
// Quantidade de pizzas
        int qty
                = Integer.parseInt(
                        jTextField3.getText());
        //valor do recheio da massa
        double rate = 0;

// Valores do recheio da massa
        if (jRadioButton1.isSelected()) {
            rate = 10;
        } else if (jRadioButton2.isSelected()) {
            rate = 20;
        } else if (jRadioButton3.isSelected()) {
            rate = 30;
        }
//Mostra o valor total do recheio
        jTextField4.setText(
                "" + rate);

        double topamt = 0;

// Cobertura da pizza
        if (jCheckBox1.isSelected()) {
            topamt = 7;
        }
        if (jCheckBox2.isSelected()) {
            topamt = topamt + 12;
        }
        if (jCheckBox3.isSelected()) {
            topamt = topamt + 15;
        }
        if (jCheckBox4.isSelected()) {
            topamt = topamt + 20;
        }

        jTextField6.setText(
                ""
                + topamt);

// Total a pagar
        double totalpayable
                = (rate * qty) + topamt;
        jTextField5.setText("" + totalpayable);

// Construtor com detalhes do pedido
        Pizza piz = new Pizza(orderno, custname, qty, rate, topamt, totalpayable);
//Escreve na area de texto todos os detalhes do pedido        
        jTextArea1.setText("" + piz.toString());

    }                                          
